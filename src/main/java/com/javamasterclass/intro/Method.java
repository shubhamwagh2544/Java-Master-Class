package com.javamasterclass.intro;

public class Method {

    /*
                access modifier : mandatory : private, public, protected or default
                static : optional
                return type : void
                name : addNumbers
                parameters : optional
                body : empty / code
                return value : void / any data_type
     */
    private static void addNumbers (String name) {
        System.out.println("My name is " + name);
    }

    public static void main(String[] args) {
        // what is method : block of code that performs certain task in order
        // how to write method
        /*
                - Access Modifier
                *- Optional static
                - Return Type
                - Method Name
                *- Optional Parameters
                - Method Body
                *- Optional Return value
         */
    }
}
